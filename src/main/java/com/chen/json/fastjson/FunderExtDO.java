public class FunderExtDO {

    private Boolean enableDisplayProduct;

    private Integer age;

    private String address;

    public Boolean getEnableDisplayProduct() {
        return enableDisplayProduct;
    }

    public void setEnableDisplayProduct(Boolean enableDisplayProduct) {
        this.enableDisplayProduct = enableDisplayProduct;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
